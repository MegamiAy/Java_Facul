package ex02;

public class Estudante {
	String nomeEstudante;
	String cursoMatriculado;
	int idadeEstudante;
	
	public Estudante() {
		
	}

	@Override
	public String toString() {
		return "Estudante [nomeEstudante=" + nomeEstudante + ", cursoMatriculado=" + cursoMatriculado
				+ ", idadeEstudante=" + idadeEstudante + "]";
	}
	
	
	
}
